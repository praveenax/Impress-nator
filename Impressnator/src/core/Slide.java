package core;

public class Slide {
	
	int slideNumber;
	String content;
	public int getSlideNumber() {
		return slideNumber;
	}
	public void setSlideNumber(int slideNumber) {
		this.slideNumber = slideNumber;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	
	
	

}
